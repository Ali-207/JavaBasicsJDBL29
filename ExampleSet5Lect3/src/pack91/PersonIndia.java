package pack91;

public class PersonIndia {

	int age;
	String fname;
	String lName;

	public PersonIndia(int age, String fname, String lName) {
		this.age = age;
		this.fname = fname;
		this.lName = lName;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}

	public String getlName() {
		return lName;
	}

	public void setlName(String lName) {
		this.lName = lName;
	}
	
	
	

}
